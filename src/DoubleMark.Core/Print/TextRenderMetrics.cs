namespace DoubleMark.Core.Print;

internal static class TextRenderMetrics
{
    private const int GlyphRows = 7;
    private const int GlyphWidth = 3;

    public static double GetScaleFactor(double fontSizePt, int dpi) =>
        Math.Max(0.35, fontSizePt * dpi / 72.0 / GlyphRows);

    public static int GetScale(double fontSizePt, int dpi) =>
        Math.Max(1, (int)Math.Round(GetScaleFactor(fontSizePt, dpi)));

    public static char NormalizeChar(char ch) =>
        ch switch
        {
            '—' or '–' => '-',
            _ => ch
        };

    public static int MeasureTextRunPx(string text, double fontSizePt, bool bold, int dpi)
    {
        var scale = GetScaleFactor(fontSizePt, dpi);
        var widthPx = 0;
        foreach (var rawCh in text.ToUpperInvariant())
            widthPx += AdvanceWidthPx(NormalizeChar(rawCh), scale, bold);
        return Math.Max(1, widthPx);
    }

    public static int AdvanceWidthPx(char ch, double scale, bool bold) =>
        TinyFont.AdvanceWidthPx(ch, scale, bold);

    public static int MeasureGlyphWidthPx(double fontSizePt, bool bold, int dpi)
    {
        var scale = GetScaleFactor(fontSizePt, dpi);
        return Math.Max(1, (int)Math.Round(GlyphWidth * scale + (bold ? 1 : 0)));
    }

    public static int MeasureGlyphHeightPx(double fontSizePt, int dpi)
    {
        var scale = GetScaleFactor(fontSizePt, dpi);
        return Math.Max(1, (int)Math.Ceiling(GlyphRows * scale));
    }

    public static double MeasureTextWidthMm(string text, double fontSizePt, bool bold, int dpi = 300) =>
        MeasureTextRunPx(text, fontSizePt, bold, dpi) * 25.4 / dpi;

    public static double MeasureTextHeightMm(double fontSizePt, int dpi = 300) =>
        MeasureGlyphHeightPx(fontSizePt, dpi) * 25.4 / dpi;
}

